public class Program5JD {

    //Employee Class
    public class Employee {
        private String name;
        private String type;
        private int hours;
        private double wage;
        private double salary;

        //Constructor
        public Employee(String name, String type, int hours, double wage, double salary) {
            this.name = name;
            this.type = type;
            this.hours = hours;
            this.wage = wage;
            this.salary = salary;
        }
    }
}
